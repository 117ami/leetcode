impl Solution {
    pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
        let (mut rmin, mut rmax, mut rmode) = (i32::max_value(), i32::min_value(), 0);
        let (mut rmean, mut rmedian) = (0_f64, 0_f64);
        let (mut cnt, mut sum, mut freq) = (0_i64, 0_i64, 0_i32);
        for (i, n) in count.iter().enumerate() {
            if *n == 0 { continue ;}
            rmin = min(rmin, *n); 
            rmax = max(rmax, *n);
            cnt += *n as i64; 
            sum += cnt * (i as i64);
            if *n > freq {
                freq = *n; 
                rmode = i; 
            }
        }
        

        vec![rmin as f64, rmax as f64, rmean, rmedian, rmode as f64]
    }
}


