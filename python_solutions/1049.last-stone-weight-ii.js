var lastStoneWeightII = function(stones) {
    var mus = sum(stones),
        l = len(stones);
    var dp = two_d_array(mus + 1, l + 1, false);
    for (let i = 0; i <= l; i++) dp[0][i] = true;
    var s2 = 0;

    for (let i = 1; i <= floor(mus / 2); i++)
        for (let j = 1; j <= l; j++) {
            if (dp[i][j - 1] || (i >= stones[j - 1] && dp[i - stones[j - 1]][j - 1])) {
                dp[i][j] = true;
                s2 = Math.max(s2, i);
            }
        }
    return mus - 2 * s2;
};

