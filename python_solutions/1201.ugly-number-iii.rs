fn gcd(a: i64, b: i64) -> i64 {
    if b == 0 {
        a
    } else {
        gcd(b, a % b)
    }
}

impl Solution {
    pub fn nth_ugly_number(n: i32, a: i32, b: i32, c: i32) -> i32 {
        let a = a as i64; 
        let b = b as i64; 
        let c = c as i64; 
        let ab = a * b / gcd(a, b);
        let bc = b * c / gcd(b, c);
        let ac = a * c / gcd(a, c);
        let abc = ab * c / gcd(ab, c);
        let (mut lo, mut hi, mut mid) = (1, 10_i64.pow(9) * 2, 1);
        while lo < hi {
            mid = lo + (hi - lo) / 2;
            let cnt = mid / a + mid / b + mid / c + mid / abc - mid / ab - mid / ac - mid / bc;
            if cnt < n as i64{
                lo = mid + 1;
            } else {
                hi = mid
            };
        }
        lo as i32
    }
}

