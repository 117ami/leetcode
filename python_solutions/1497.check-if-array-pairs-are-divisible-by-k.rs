impl Solution {
    pub fn can_arrange(arr: Vec<i32>, k: i32) -> bool {
        let mut cc = vec![0; k as usize + 1];
        for a in arr {
            let i = (a % k + k) % k;
            if cc[(k - i) as usize] > 0 {
                cc[(k - i) as usize] -= 1;
            } else {
                cc[i as usize] += 1;
            }
        }
        // if cc[0] % 2 == 1 {
        //     return false;
        // }
        cc[0] % 2 == 0 && cc[1..].iter().all(|&n| n == 0)
    }
}

