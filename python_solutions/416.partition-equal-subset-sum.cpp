using VI = vector<int>;
int sum_(VI &a) { return accumulate(a.begin(), a.end(), 0); }

class Solution {
public:
    bool canPartition(vector<int>& nums) {
      int sum = sum_(nums); 
      bitset<10001> bs(1);
      for (auto &n: nums) {
        bs |= bs<<n; 
      }
      return !(sum % 2) && bs[sum/2];
    }
};


