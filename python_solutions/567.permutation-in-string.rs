static CHARHASH: [i32; 26] = [-9536, -6688, 2006, -2069, 7302, -8825, -8832, 7678, 4540, 7567, 5286, 7027, -8601, -7555, -4541, 6134, 9023, 7805, -3888, 8309, -5265, 7487, -2988, 292, -5646, 7002];
// [522, -487, -211, 337, 998, -461, 858, -660, -979, -474, -193, -911, 19, -675, -271, -993, -277, 834, -649, -782, 629, -188, 598, -351, 969, 17,];

pub fn hash_string(s: String) -> i32 {
    s.chars().map(|c| CHARHASH[char2usize(c)]).sum()
}

impl Solution {
    pub fn check_inclusion(s1: String, s2: String) -> bool {
        let (l1, l2) = (s1.len(), s2.len());
        if l1 > l2 { return false }
        let hs1 = hash_string(s1); 
        let mut hs2 = hash_string(s2[..l1].to_string());
        if hs1 == hs2 { return true }

        let chars_2 = s2.chars().collect::<Vec<_>>();
        for i in 0..l2-l1 {
            hs2 += CHARHASH[char2usize(chars_2[i + l1])] - CHARHASH[char2usize(chars_2[i])];
            if hs2 == hs1 { return true }
        }
        false
    }
}


