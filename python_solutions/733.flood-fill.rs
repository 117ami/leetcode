/*
 * @lc app=leetcode id=733 lang=rust
 *
 * [733] Flood Fill
 *
 * https://leetcode.com/problems/flood-fill/description/
 *
 * algorithms
 * Easy (53.61%)
 * Total Accepted:    106.7K
 * Total Submissions: 198.4K
 * Testcase Example:  '[[1,1,1],[1,1,0],[1,0,1]]\n1\n1\n2'
 *
 *
 * An image is represented by a 2-D array of integers, each integer
 * representing the pixel value of the image (from 0 to 65535).
 *
 * Given a coordinate (sr, sc) representing the starting pixel (row and column)
 * of the flood fill, and a pixel value newColor, "flood fill" the image.
 *
 * To perform a "flood fill", consider the starting pixel, plus any pixels
 * connected 4-directionally to the starting pixel of the same color as the
 * starting pixel, plus any pixels connected 4-directionally to those pixels
 * (also with the same color as the starting pixel), and so on.  Replace the
 * color of all of the aforementioned pixels with the newColor.
 *
 * At the end, return the modified image.
 *
 * Example 1:
 *
 * Input:
 * image = [[1,1,1],[1,1,0],[1,0,1]]
 * sr = 1, sc = 1, newColor = 2
 * Output: [[2,2,2],[2,2,0],[2,0,1]]
 * Explanation:
 * From the center of the image (with position (sr, sc) = (1, 1)), all pixels
 * connected
 * by a path of the same color as the starting pixel are colored with the new
 * color.
 * Note the bottom corner is not colored 2, because it is not 4-directionally
 * connected
 * to the starting pixel.
 *
 *
 *
 * Note:
 * The length of image and image[0] will be in the range [1, 50].
 * The given starting pixel will satisfy 0  and 0 .
 * The value of each color in image[i][j] and newColor will be an integer in
 * [0, 65535].
 *
 */
impl Solution {
    pub fn flood_fill(mut image: Vec<Vec<i32>>, sr: i32, sc: i32, new_color: i32) -> Vec<Vec<i32>> {
        let (sr, sc) = (sr as usize, sc as usize);
        let color = image[sr][sc];
        let mut cc: HashSet<(usize, usize)> = HashSet::new();
        let mut jobs: VecDeque<(usize, usize)> = VecDeque::from_iter(vec![(sr, sc)]);
        let dirs = vec![-1, 0, 1, 0, -1];

        while !jobs.is_empty() {
            let top = jobs.pop_front().unwrap();
            if cc.contains(&top) || image[top.0][top.1] != color {
                continue;
            };
            cc.insert(top);
            image[top.0][top.1] = new_color;
            for i in 0..4 {
                let j = top.0 as i32 + dirs[i];
                let k = top.1 as i32 + dirs[i + 1];
                if 0 <= j && j < image.len() as i32 && 0 <= k && k < image[0].len() as i32 {
                    jobs.push_back((j as usize, k as usize));
                }
            }
        }

        image
    }
}

