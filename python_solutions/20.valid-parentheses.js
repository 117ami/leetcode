var isValid = function(s) {
    var stack = list(len(s), 0),
        i = 0,
        b = true;
    [...s].forEach(function(c) {
        if (c == '(' || c == '[' || c == '{') {
            stack[i] = c;
            i += 1;
        } else if (i == 0 || (c == ']' && stack[i - 1] != '[') || (c == '}' && stack[i - 1] != '{') || (c == ')' && stack[i - 1] != '(')) {
            b = false;
            return;
        } else {
            i -= 1;
        }
    })
    return b && i == 0;
};

