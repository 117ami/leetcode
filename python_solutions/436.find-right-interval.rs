impl Solution {
    pub fn find_right_interval(ns: Vec<Vec<i32>>) -> Vec<i32> {
        let mut sp = ns
            .iter()
            .enumerate()
            .map(|(i, n)| vec![i as i32, n[0]])
            .collect::<Vec<Vec<_>>>();
        sp.sort_unstable_by(|a, b| a[1].cmp(&b[1]));
        let i32sp = sp.iter().map(|p| p[1]).collect::<Vec<_>>();
        let size = ns.len();
        // println!("{:?} {:?}", sp, i32sp);
        let mut res = vec![0; size];

        for (i, p) in ns.iter().enumerate() {
            let j = bisect_left(&i32sp, p[1]);
            // println!("{} {:?}", j, p);
            res[i] = if j < size { sp[j][0] } else { -1 };
        }
        res
    }
}

