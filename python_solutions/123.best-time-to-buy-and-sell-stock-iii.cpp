/*
 * @lc app=leetcode id=123 lang=cpp
 *
 * [123] Best Time to Buy and Sell Stock III
 *
 * https://leetcode.com/problems/best-time-to-buy-and-sell-stock-iii/description/
 *
 * algorithms
 * Hard (38.42%)
 * Total Accepted:    240.8K
 * Total Submissions: 626.1K
 * Testcase Example:  '[3,3,5,0,0,3,1,4]'
 *
 * Say you have an array for which the i^th element is the price of a given
 * stock on day i.
 *
 * Design an algorithm to find the maximum profit. You may complete at most two
 * transactions.
 *
 * Note: You may not engage in multiple transactions at the same time (i.e.,
 * you must sell the stock before you buy again).
 *
 *
 * Example 1:
 *
 *
 * Input: prices = [3,3,5,0,0,3,1,4]
 * Output: 6
 * Explanation: Buy on day 4 (price = 0) and sell on day 6 (price = 3), profit
 * = 3-0 = 3.
 * Then buy on day 7 (price = 1) and sell on day 8 (price = 4), profit = 4-1 =
 * 3.
 *
 * Example 2:
 *
 *
 * Input: prices = [1,2,3,4,5]
 * Output: 4
 * Explanation: Buy on day 1 (price = 1) and sell on day 5 (price = 5), profit
 * = 5-1 = 4.
 * Note that you cannot buy on day 1, buy on day 2 and sell them later, as you
 * are engaging multiple transactions at the same time. You must sell before
 * buying again.
 *
 *
 * Example 3:
 *
 *
 * Input: prices = [7,6,4,3,1]
 * Output: 0
 * Explanation: In this case, no transaction is done, i.e. max profit = 0.
 *
 *
 * Example 4:
 *
 *
 * Input: prices = [1]
 * Output: 0
 *
 *
 *
 * Constraints:
 *
 *
 * 1 <= prices.length <= 10^5
 * 0 <= prices[i] <= 10^5
 *
 *
 */
class Solution {
public:
  int maxProfit(vector<int> &prices) {
    /* dp[i][j] is the max profit up until prices[j] using at most i
    transactions. dp[i][j] = for m in [0, j - 1]: max(dp[i][j-1], prices[j] -
    prices[m] + dp[i-1][m]) = max(dp[i][j-1], prices[j] + max(dp[i-1][m] -
    prices[m]))


    dp[0][j] = 0, no transcation no profit
    dp[i][0] = 0, no price data, no profit
    */
    int n = prices.size();
    if (n <= 1)
      return 0;
    int k = 2;
    int dp[k + 1][n];
    memset(dp, 0, sizeof(dp));

    for (int i = 1; i <= k; i++) {
      int tmp_max = -prices[0];
      for (int j = 1; j < n; j++) {
        dp[i][j] = max(dp[i][j - 1], prices[j] + tmp_max);
        tmp_max = max(tmp_max, dp[i - 1][j] - prices[j]);
      }
    }

    return dp[k][n - 1];
  }
};

