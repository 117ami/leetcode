class Solution:
    def sampleStats(self, count: List[int]) -> List[float]:
        self.min, self.max, self.mean, self.median, self.mode = float('inf'), float('-inf'), 0, 0, float('-inf')
        max_cnt, all_sum, all_count = 0, 0, 0
        median = []
        for i, c in enumerate(count):
            if c == 0:continue
            all_sum += c * i
            all_count += c
            median.append((all_count, i))
            self.min = min(self.min, i)
            self.max = max(self.max, i)

            if c > max_cnt:
                self.mode = i
                max_cnt = c

        self.mean = all_sum / all_count
        mid = (all_count + 1) // 2

        i = bisect_left([c for c, _ in median], mid)
        j = bisect_left([c for c, _ in median], mid + 1)
        left = median[i][1] if median[i][0] >= mid else median[i + 1][1]
        right = median[j][1] if median[j][0] >= mid + 1 else median[j + 1][1]

        self.median = left if all_count % 2 == 1 else (left + right) / 2
        return [self.min, self.max, self.mean, self.median, self.mode]


