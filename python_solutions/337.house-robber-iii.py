# Definition for a binary tree node.
class TreeNode:
    def __init__(self, x):
        self.val = x
        self.left = None
        self.right = None

class Solution:
    def rob(self, root: TreeNode) -> int:
        def rec(node):
            if not node: return (0, 0)
            lv, rv = rec(node.left), rec(node.right)
            rob = norob = 0
            rob = node.val + lv[1] + rv[1]
            norob = max(lv) + max(rv)
            return (rob, norob)

        return max(rec(root))
        

